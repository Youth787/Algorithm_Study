class Solution {
    public int solution(int num1, int num2) {
        return num1+num2;
    }

    public int solution(int num1, int num2) {
        return num1-num2;
    }

    public int solution(int num1, int num2) {
        return num1*num2;
    }

    public int solution(int num1, int num2) {
        return num1/num2;
    }

    public int solution(int num1, int num2) {
        int num = (num1*1000)/num2;
        return num;
    }
}
